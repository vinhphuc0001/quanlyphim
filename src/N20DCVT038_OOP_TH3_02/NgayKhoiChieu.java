package N20DCVT038_OOP_TH3_02;

public class NgayKhoiChieu {
	 private int ngay;
	    private int thang;
	    private int nam;

	    public NgayKhoiChieu(int ngay, int thang, int nam) {
	        this.ngay = ngay;
	        this.thang = thang;
	        this.nam = nam;
	    }

	    public int getNgay() {
	        return ngay;
	    }

	    public int getThang() {
	        return thang;
	    }

	    public int getNam() {
	        return nam;
	    }
	}

